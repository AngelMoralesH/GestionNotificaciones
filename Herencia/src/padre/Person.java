package padre;

public class Person {
    protected String name;
    private int edad;

    public Person(){
        System.out.println("Person");
    }
    public Person(String name, int edad) {
        this.name = name;
        this.edad = edad;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getEdad() {
        return edad;
    }

    public void setEdad(int edad) {
        this.edad = edad;
    }
}
